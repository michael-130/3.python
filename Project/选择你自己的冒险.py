# 选择你自己的冒险游戏

print("欢迎来到选择你自己的冒险游戏！")
print("你站在一个神秘的森林入口前...")

选择1 = input("你想要进入森林还是绕过它？输入'进入'或'绕过'：").lower()

if 选择1 == "进入":
    print("你勇敢地走进了森林...")
    print("你看到了一条分叉的小径。")
    
    选择2 = input("你想要走左边的路还是右边的路？输入'左'或'右'：").lower()
    
    if 选择2 == "左":
        print("你走向左边，发现了一个宝藏箱！")
        print("恭喜你！你找到了黄金！")
    elif 选择2 == "右":
        print("你走向右边，遇到了一只友善的独角兽。")
        print("独角兽带你飞向了魔法王国！")
    else:
        print("无效的选择。游戏结束。")

elif 选择1 == "绕过":
    print("你选择绕过森林，沿着海岸线行走。")
    print("你发现了一艘废弃的船。")
    
    选择3 = input("你想要登上船还是继续沿着海岸走？输入'登船'或'继续'：").lower()
    
    if 选择3 == "登船":
        print("你登上了船，发现了一张藏宝图！")
        print("你成为了一名海盗船长！")
    elif 选择3 == "继续":
        print("你继续沿着海岸走，找到了一个美丽的村庄。")
        print("村民们欢迎你成为他们的新朋友！")
    else:
        print("无效的选择。游戏结束。")

else:
    print("无效的选择。游戏结束。")

print("感谢你玩这个游戏！")